public class switch_statement {
    public static void main(String args[]){
        int num=21;
        switch(num)
        {
            case 1: System.out.println("Get XUV700");
            break;
            case 2: System.out.println("Get BENZ");
            break;
            case 3: System.out.println("Get BMW");
            break;
            default: System.out.println("Back to reality");
        }
    }
    
}
