public class unary {
    public static void main(String args[]){
        
        //pre and post increment
        int a=10;
        //int b=++a;
        int b=a++;
        System.out.println(a);
        System.out.println(b);

        //pre and post decrement

        int x=10;
        // int y=--x;
        int y=x--;
        System.out.println(x);
        System.out.println(y);

    }
    
}
